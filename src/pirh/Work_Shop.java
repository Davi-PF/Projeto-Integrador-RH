    private void jPasswordField_codsegFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField_codsegFocusGained
        // TODO add your handling code here:
        
        // Limpa o textfield quando está em foco se o texto for "Usuário"
        if(jPasswordField_codseg.getText().trim().equals("Código de Segurança"))
        {
            jPasswordField_codseg.setText("");
            jPasswordField_codseg.setForeground(Color.black);
        }
        
        // Coloca uma borda amarela no icone do jLabel
        Border jlabel_icon = BorderFactory.createMatteBorder(1, 1, 1, 1, new Color(51,51,51));
        jLabel_username2.setBorder(jlabel_icon);
        
        
    }//GEN-LAST:event_jPasswordField_codsegFocusGained

    private void jPasswordField_codsegFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField_codsegFocusLost
        // TODO add your handling code here:
        
        
        // se selecionar senha sem colocar nada em usuario, ele volta ao normal
       // será 'setado' usuário no textfield na perca de foco
       
       if(jPasswordField_codseg.getText().trim().equals("") ||
          jPasswordField_codseg.getText().trim().equals("Código de Segurança"))
       {
          jPasswordField_codseg.setText("Código de Segurança");
          jPasswordField_codseg.setForeground(new Color(153,153,153));
       }
       
       // create a border with the color of the parent jpanel for the icons label to remove the move
        Border label_icons_border = BorderFactory.createMatteBorder(0, 1, 1, 1, new Color(102,102,102));
        jLabel_username2.setBorder(label_icons_border);
        
        
        
    }//GEN-LAST:event_jPasswordField_codsegFocusLost


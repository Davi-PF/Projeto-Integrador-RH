    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        PreparedStatement st;
        ResultSet rs;
        
        String codigo_seguranca = jPasswordField_codseg.getText();
        
        String query = "SELECT * FROM `area_segu` WHERE `codigo_seguranca` = ?";
        
        if(codigo_seguranca.trim().equals("Código de Segurança") || codigo_seguranca.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Digite o seu Código de Segurança","Alerta",2);
            
        } else{
            
            try {
                st = My_CNX2.getConnection().prepareStatement(query);
                
                st.setString(1, codigo_seguranca);
                rs = st.executeQuery();
                
                if(rs.next())
            {
                // mostra um novo form
                Area_seguranca form1 = new Area_seguranca();
                form1.setVisible(true);
                form1.pack();
                form1.setLocationRelativeTo(null);
                
                this.dispose();
                
            } else{
                // mensagem de erro
                JOptionPane.showMessageDialog(null, "Código Invalido", "Login Error",2);
                
            }
                
            } catch (SQLException ex) {
                Logger.getLogger(Work_Shop.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

